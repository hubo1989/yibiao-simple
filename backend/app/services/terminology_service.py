class TerminologyService:
    """行业术语库服务"""

    @staticmethod
    def detect_industry(project_title: str = "", project_description: str = "") -> Optional[str]:
        """
        根据项目标题和描述检测行业

        Args:
            project_title: 项目标题
            project_description: 项目描述

        Returns:
            行业名称，未检测到时返回 None
        """
        combined_text = f"{project_title or ''} {project_description or ''}".lower()
        if not combined_text.strip():
            return None

        scores: Dict[str, int] = {}
        for industry, keywords in INDUSTRY_KEYWORDS.items():
            score = sum(1 for kw in keywords if kw.lower() in combined_text)
            if score > 0:
                scores[industry] = score

        if not scores:
            return None

        # 返回得分最高的行业
        return max(scores, key=scores.get)

    @staticmethod
    def get_terminology(industry: str) -> Dict[str, str]:
        """
        获取指定行业的术语表

        Args:
            industry: 行业名称

        Returns:
            术语字典 {term: standard_expression}
        """
        return INDUSTRY_TERMINOLOGY.get(industry, {})

    @staticmethod
    def get_terminology_guide(industry: str) -> str:
        """
        获取指定行业的术语使用指南文本，用于注入 prompt

        Args:
            industry: 行业名称

        Returns:
            格式化的术语指南文本
        """
        terms = TerminologyService.get_terminology(industry)
        if not terms:
            return ""

        lines = ["本章写作必须使用以下标准术语，不得自创替代说法："]
        for term, standard in sorted(terms.items()):
            lines.append(f"- {term} → {standard}")

        lines.append("")
        lines.append("若需介绍上述术语相关的内容，请使用标准表达，首次出现时建议附带全称。")
        return "\n".join(lines)

    @staticmethod
    def get_all_industries() -> list[str]:
        """获取所有支持的行业列表"""
        return list(INDUSTRY_TERMINOLOGY.keys())

    @staticmethod
    def get_industry_keywords(industry: str) -> list[str]:
        """获取指定行业的关键词列表"""
        return INDUSTRY_KEYWORDS.get(industry, [])
